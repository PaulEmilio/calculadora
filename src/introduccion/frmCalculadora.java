    private void jbtIgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtIgualActionPerformed
        // TODO add your handling code here:
        this.Segundonumero=Float.parseFloat(this.Casilla.getText());
        switch(this.Operador){
            case"+":this.Casilla.setText(sincero(this.Primernumero+ this.Segundonumero));
         break;
         case"-":this.Casilla.setText(sincero(this.Primernumero- this.Segundonumero));
         break;
             
         
       
         
               
         case"*":this.Casilla.setText(sincero(this.Primernumero * this.Segundonumero));
         break;
         case"/":if(this.Segundonumero==0){this.Casilla.setText("NoSeDivideEntreCero");}
         else{
                 this.Casilla.setText(sincero(this.Primernumero/ this.Segundonumero));
         break;
         }
         
         
                    
        }
    }//GEN-LAST:event_jbtIgualActionPerformed

